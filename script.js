            '—Å–≤—è–∑—å|–∫–æ–Ω—Ç–∞–∫—Ç|—Ç–µ–ª–µ–≥—Ä–∞–º|–∏–Ω—Å—Ç–∞–≥—Ä–∞–º': knowledgeBase.–∫–æ–Ω—Ç–∞–∫—Ç—ã,
            '—Ñ–∏–ª–æ—Å–æ—Ñ|–ø—Ä–∏–Ω—Ü–∏–ø|–ø–æ–¥—Ö–æ–¥': knowledgeBase.—Ñ–∏–ª–æ—Å–æ—Ñ–∏—è,
